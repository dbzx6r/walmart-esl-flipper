// Map char to small font index
static int _small_char_idx(char c) {
    if(c >= '0' && c <= '9') return c - '0';
    if(c == '$') return 10;
    if(c == '.') return 11;
    if(c == ' ') return 12;
    if(c == '-') return 13;
    return 14; // unknown
}

static void _draw_small_glyph(EslImageBuffer* buf, int x, int y, int idx) {
    EslDisplaySize sz = esl_model_size(buf->model);
    const uint8_t* g = SMALL_FONT[idx];
    for(int row = 0; row < SMALL_FONT_H; row++) {
        for(int col = 0; col < SMALL_FONT_W; col++) {
            int px = x + col;
            int py = y + row;
            if(px < 0 || px >= sz.width || py < 0 || py >= sz.height) continue;
            if((g[row] >> (7 - col)) & 1) {
                esl_buffer_set_pixel(buf, (uint16_t)px, (uint16_t)py, true);
            }
        }
    }
}

// ── Price rendering ──────────────────────────────────────────────────────────

void esl_render_price(EslImageBuffer* buf, const char* price, const char* label) {
    EslDisplaySize sz = esl_model_size(buf->model);

    // Clear to white
    esl_buffer_init(buf, buf->model);

    // Draw thin border
    for(int x = 0; x < sz.width; x++) {
        esl_buffer_set_pixel(buf, x,  0,            true);
        esl_buffer_set_pixel(buf, x,  1,            true);
        esl_buffer_set_pixel(buf, x,  sz.height - 1, true);
        esl_buffer_set_pixel(buf, x,  sz.height - 2, true);
    }
    for(int y = 0; y < sz.height; y++) {
        esl_buffer_set_pixel(buf, 0,           y, true);
        esl_buffer_set_pixel(buf, 1,           y, true);
        esl_buffer_set_pixel(buf, sz.width - 1, y, true);
        esl_buffer_set_pixel(buf, sz.width - 2, y, true);
    }

    int y_offset = 4;

    // Draw label (small font) if present
    if(label && label[0] != '\0') {
        int label_len = 0;
        for(int i = 0; label[i]; i++) label_len++;

        int label_px_w = label_len * (SMALL_FONT_W + 1);
        int lx = (sz.width - label_px_w) / 2;
        if(lx < 4) lx = 4;

        for(int i = 0; label[i]; i++) {
            _draw_small_glyph(buf, lx + i * (SMALL_FONT_W + 1), y_offset, _small_char_idx(label[i]));
        }
        y_offset += SMALL_FONT_H + 4;

        // Separator line
        for(int x = 4; x < sz.width - 4; x++) {
            esl_buffer_set_pixel(buf, x, y_offset, true);
        }
        y_offset += 3;
    }

    // Draw price (large font) — centred
    int price_len = 0;
    for(int i = 0; price[i]; i++) price_len++;

    // Scale font to fit: default full size or scaled down
    int available_w = sz.width - 8;
    int glyph_stride = FONT_W + 2;
    int price_px_w   = price_len * glyph_stride;
    int price_y      = y_offset + 2;

    // Clamp to available space
    int px = (available_w - price_px_w) / 2 + 4;
    if(px < 4) px = 4;

    for(int i = 0; price[i]; i++) {
        int idx = _char_to_font_idx(price[i]);
        _draw_glyph(buf, px + i * glyph_stride, price_y, idx);
    }
}

// ── BLE command builders ──────────────────────────────────────────────────────

uint8_t esl_cmd_clear(uint8_t* out_buf, uint8_t fill) {
    out_buf[0] = ESL_CMD_CLEAR;
    out_buf[1] = fill;
    return 2;
}

uint8_t esl_cmd_set_pos(uint8_t* out_buf, uint16_t pos) {
    out_buf[0] = ESL_CMD_SET_POS;
    out_buf[1] = (uint8_t)(pos >> 8);
    out_buf[2] = (uint8_t)(pos & 0xFF);
    return 3;
}

uint8_t esl_cmd_write(uint8_t* out_buf, const uint8_t* data, uint8_t data_len) {
    out_buf[0] = ESL_CMD_WRITE;
    for(uint8_t i = 0; i < data_len; i++) {
        out_buf[1 + i] = data[i];
    }
    return 1 + data_len;
}

uint8_t esl_cmd_display(uint8_t* out_buf) {
    out_buf[0] = ESL_CMD_DISPLAY;
    return 1;
}
