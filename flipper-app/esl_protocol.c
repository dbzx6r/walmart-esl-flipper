// ── Display model geometry ───────────────────────────────────────────────────

EslDisplaySize esl_model_size(EslDisplayModel model) {
    switch(model) {
    case EslModelBW213:    return (EslDisplaySize){250, 122};
    case EslModelBWR213:   return (EslDisplaySize){250, 122};
    case EslModelBWR154:   return (EslDisplaySize){200, 200};
    case EslModelBW213ICE: return (EslDisplaySize){212, 104};
    case EslModelBWR350:   return (EslDisplaySize){250, 128};
    case EslModelBWY350:   return (EslDisplaySize){250, 128};
    default:               return (EslDisplaySize){250, 122};
    }
}

uint32_t esl_buffer_size(EslDisplayModel model) {
    EslDisplaySize sz = esl_model_size(model);
    uint16_t col_bytes = (sz.height + 7) / 8;
    return (uint32_t)sz.width * col_bytes;
}

// ── Image buffer ─────────────────────────────────────────────────────────────

void esl_buffer_init(EslImageBuffer* buf, EslDisplayModel model) {
    buf->model = model;
    buf->size  = esl_buffer_size(model);
    // 0xFF = all white (bit=1 means white in ATC firmware)
    memset(buf->data, 0xFF, buf->size);
}

void esl_buffer_set_pixel(EslImageBuffer* buf, uint16_t x, uint16_t y, bool black) {
    EslDisplaySize sz = esl_model_size(buf->model);
    if(x >= sz.width || y >= sz.height) return;

    uint16_t col_bytes  = (sz.height + 7) / 8;
    uint32_t byte_index = (uint32_t)(sz.width - 1 - x) * col_bytes + (y >> 3);
    uint8_t  bit_mask   = 0x80 >> (y & 7);

    if(byte_index >= buf->size) return;

    if(black) {
        buf->data[byte_index] &= ~bit_mask;  // clear bit = black
    } else {
        buf->data[byte_index] |= bit_mask;   // set bit = white
    }
}

