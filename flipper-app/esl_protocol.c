// Map ASCII char to font index
static int _char_to_font_idx(char c) {
    if(c >= '0' && c <= '9') return c - '0';
    switch(c) {
    case '.': return CH_DOT;
    case '$': return CH_DLR;
    case ' ': return CH_SPC;
    case '-': return CH_MIN;
    case '%': return CH_PCT;
    case 'S': case 's': return CH_S;
    case 'A': case 'a': return CH_A;
    case 'L': case 'l': return CH_L;
    case 'E': case 'e': return CH_E;
    case 'O': case 'o': return CH_O;
    case 'F': case 'f': return CH_F;
    default:  return CH_UNKNOWN;
    }
}

// Draw a single character glyph at (x, y) into the ESL buffer
static void _draw_glyph(EslImageBuffer* buf, int x, int y, int font_idx) {
    EslDisplaySize sz = esl_model_size(buf->model);
    const uint8_t* glyph = FONT_DATA[font_idx];

    for(int row = 0; row < FONT_H; row++) {
        uint16_t row_bits = ((uint16_t)glyph[row * 2] << 8) | glyph[row * 2 + 1];
        for(int col = 0; col < FONT_W; col++) {
            int px = x + col;
            int py = y + row;
            if(px < 0 || px >= sz.width || py < 0 || py >= sz.height) continue;
            bool black = (row_bits >> (15 - col)) & 1;
            if(black) {
                esl_buffer_set_pixel(buf, (uint16_t)px, (uint16_t)py, true);
            }
        }
    }
}

